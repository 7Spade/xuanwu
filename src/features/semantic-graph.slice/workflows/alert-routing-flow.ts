/** Classification of a semantic graph alert. */
export type AlertKind = 'STALE_TAG' | 'INVARIANT_FAIL' | 'CAUSALITY_WARN';

/** An alert generated by the semantic graph. */
export interface SemanticGraphAlert {
  /** Unique alert identifier. */
  readonly alertId: string;
  /** Classification of the alert. */
  readonly kind: AlertKind;
  /** The semantic tag slug involved (if applicable). */
  readonly tagSlug?: TagSlugRef;
  /** Human-readable alert message. */
  readonly message: string;
  /** ISO-8601 timestamp when the alert was generated. */
  readonly detectedAt: string;
  /** Severity: 0 (info) ??100 (critical). */
  readonly severity: number;
}

/** Result of routing an alert through the dispatch layer. */
export interface AlertRoutingResult {
  readonly alert: SemanticGraphAlert;
  /** Dispatch command produced, if a policy matched. */
  readonly dispatchCommand: DispatchCommand | null;
  /** Whether a dispatch policy was found for this alert. */
  readonly policyFound: boolean;
  /** Reason dispatch was skipped (if policyFound is false). */
  readonly skipReason?: string;
}

