/**
 * Route a pre-built `SemanticGraphAlert` through the policy-mapper dispatch layer.
 *
 * If the alert carries a `tagSlug`, the policy-mapper is consulted.
 * If no policy is found, `policyFound` is `false` and `dispatchCommand` is `null`.
 *
 * [D21-5] VS8_ROUT routing arc.
 * [D27-A] ??dispatch goes through policy-mapper; no ID hard-coding.
 */
export function routeAlert(alert: SemanticGraphAlert): AlertRoutingResult {
  if (!alert.tagSlug) {
    return { alert, dispatchCommand: null, policyFound: false, skipReason: 'No tagSlug on alert.' };
  }

  const resolution = resolveDispatchPolicy(alert.tagSlug);
  if (!resolution.found) {
    return { alert, dispatchCommand: null, policyFound: false, skipReason: resolution.reason };
  }

  const dispatchResult = dispatchForTag(alert.tagSlug);
  return {
    alert,
    dispatchCommand: dispatchResult.success ? dispatchResult.command : null,
    policyFound: true,
  };
}

/**
 * Create and route a stale-tag alert [S4].
 */
export function routeStaleTagAlert(
  tagSlug: TagSlugRef,
  staleAgeMs: number
): AlertRoutingResult {
  const alert: SemanticGraphAlert = {
    alertId: _generateAlertId('STALE_TAG'),
    kind: 'STALE_TAG',
    tagSlug,
    message: `Tag "${tagSlug as string}" has been stale for ${Math.floor(staleAgeMs / 86_400_000)} days.`,
    detectedAt: new Date().toISOString(),
    severity: Math.min(100, Math.floor(staleAgeMs / 86_400_000)),
  };
  return routeAlert(alert);
}

